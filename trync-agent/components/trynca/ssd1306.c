void ssd1306_command(uint8_t command) {

	esp_err_t ret = i2c_write_byte(SSD1306_ADDRESS, command);
	if (ret != ESP_OK) {
		ESP_LOGE("OLED", "OLED Command %X error (ERR = %d)", command, ret);
	}
	vTaskDelay(5 / portTICK_PERIOD_MS);
}

void OLEDSetPos(uint8_t x, uint8_t y) {
	ssd1306_command(0xb0 + y);
	ssd1306_command(((x & 0xf0) >> 4) | 0x10);
	ssd1306_command((x & 0x0f) | 0x00);
}

